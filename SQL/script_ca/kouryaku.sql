/* 徹底攻略 Bronze問題集用・ Emp表、Dept表*/


/** テーブルがひとつでも存在している場合、削除する **/

DECLARE
  v_cnt NUMBER;
BEGIN
  SELECT count(table_name)  INTO v_cnt
  FROM user_tables
  WHERE table_name IN ('EMP','DEPT','SALGRADE','JOB');

  IF v_cnt > 0 THEN
    EXECUTE IMMEDIATE 'DROP TABLE EMP purge';
    EXECUTE IMMEDIATE 'DROP TABLE DEPT purge';
    EXECUTE IMMEDIATE 'DROP TABLE SALGRADE purge';
    EXECUTE IMMEDIATE 'DROP TABLE JOB purge';
  END IF;
END;
/


          -- 実習用の表−−部署表を作成します。
CREATE TABLE DEPT(
	DEPTNO	NUMBER(4)     CONSTRAINT PK_DEPT_NO PRIMARY KEY,
	DNAME	VARCHAR2(12),
	LOC		VARCHAR2(12)
);

          -- 実習用の表−−社員表を作成します。
CREATE TABLE EMP(
	EMPNO		NUMBER(6)	CONSTRAINT PK_EMP_NO PRIMARY KEY,
	ENAME		VARCHAR2(10),
	JOB			VARCHAR2(10),
	MGR			NUMBER(4),
	HIREDATE	DATE		DEFAULT SYSDATE,
	SAL			NUMBER(9),
	COMM		NUMBER(9),
	DEPTNO		NUMBER(4)	CONSTRAINT FK_DEPT_NO REFERENCES DEPT(DEPTNO)
);


          -- 実習用の表−−ランク表を作成します。
CREATE TABLE SALGRADE(
	GRADE		NUMBER(2)	CONSTRAINT PK_SAL_GRADE PRIMARY KEY,
	LOSAL		NUMBER(9),
	HISAL		NUMBER(9)
);


          -- 実習用の表−−職種表を作成します。
CREATE TABLE JOB(
	JOBNO		NUMBER(3)	CONSTRAINT PK_JOB_NO PRIMARY KEY,
	JNAME		VARCHAR2(10)
);


