/** テーブルがひとつでも存在している場合、削除する **/

DECLARE
  v_cnt NUMBER;
BEGIN
  SELECT count(table_name)  INTO v_cnt
  FROM user_tables
  WHERE table_name IN ('SALES_ITEMS','SALES','CUSTOMERS','PRODUCTS','TASK','PROMOTIONS','PROMOTION_CATEGORIES');

  IF v_cnt > 0 THEN
    EXECUTE IMMEDIATE 'DROP TABLE Sales_items purge';
    EXECUTE IMMEDIATE 'DROP TABLE Sales purge';
    EXECUTE IMMEDIATE 'DROP TABLE Customers purge';
    EXECUTE IMMEDIATE 'DROP TABLE Products purge';
    EXECUTE IMMEDIATE 'DROP TABLE Task purge';
    EXECUTE IMMEDIATE 'DROP TABLE promotions purge';
    EXECUTE IMMEDIATE 'DROP TABLE promotion_categories purge';
  END IF;
END;
/


-- 顧客表 --

CREATE TABLE Customers (
CUST_ID                NUMBER(6) Constraint    CUSTOMERS_PK primary key CONSTRAINT customer_id_min check(cust_id > 0),
CUST_FIRST_NAME        VARCHAR2(20)            NOT NULL,
CUST_LAST_NAME         VARCHAR2(20)            NOT NULL,
CREDIT_LIMIT           NUMBER(9,2)             Constraint CUSTOMER_CREDIT_LIMIT_MAX CHECK(credit_limit <= 5000),
CUST_EMAIL             VARCHAR2(30),
ACCOUNT_MGR_ID         NUMBER(6)               Constraint fk_personid  References  person(personid),
DATE_OF_BIRTH          DATE ,
MARITAL_STATUS         VARCHAR2(20) ,
GENDER                 VARCHAR2(1),
INCOME_LEVEL           VARCHAR2(20)
);


-- 製品表 --

CREATE TABLE Products (
PROD_ID               NUMBER(6)                CONSTRAINT pk_product_sales  PRIMARY KEY,
PROD_NAME             VARCHAR2(25),
CATEGORY_ID           NUMBER(2),
WEIGHT_CLASS          NUMBER(1),
WARRANTY_PERIOD       INTERVAL YEAR(2) TO MONTH,
SUPPLIER_ID           NUMBER(6),
PROD_STATUS           VARCHAR2(20)             CONSTRAINT ck_prod_status   CHECK( prod_status in('orderable' ,'planned' ,'under development' ,'obsolete') ),
LIST_PRICE            NUMBER(8,2),
MIN_PRICE             NUMBER(8,2),
CATALOG_URL           VARCHAR2(50)
);


-- 売上表 --

CREATE TABLE Sales (
Sales_id             NUMBER(12)               CONSTRAINT PK_Sales primary key,
Time_id              TIMESTAMP(6) WITH LOCAL TIME ZONE    NOT NULL,
Sales_mode           VARCHAR2(8),
Cust_id              NUMBER(6)                NOT NULL,
Sales_status         NUMBER(2),
Sales_total          NUMBER(8,2),
Sales_rep_id         NUMBER(6),
Promo_id             NUMBER(6)
);


-- 売上明細表 --

CREATE TABLE Sales_items (
SALES_ID             NUMBER(12),
LINE_ITEM_ID         NUMBER(3)                NOT NULL,
PROD_ID              NUMBER(6)                NOT NULL,
UNIT_PRICE           NUMBER(8,2),
QUANTITY             NUMBER(8),
CONSTRAINT SALE_ITEMS_PK  PRIMARY KEY(SALES_ID, LINE_ITEM_ID)
);


-- 販売促進カテゴリー表 --
CREATE TABLE promotion_categories (
PROMO_CATEGORY_ID      NUMBER(2),
PROMO_CATEGORY         VARCHAR2(30)     NOT NULL,
PROMO_SUBCATEGORY_ID   NUMBER(2),
PROMO_SUBCATEGORY      VARCHAR2(30)     NOT NULL,
CONSTRAINT pk_promo_category_id         PRIMARY KEY(PROMO_CATEGORY_ID, PROMO_SUBCATEGORY_ID)
);


-- 販売促進企画表 --
CREATE TABLE promotions (
PROMO_ID              NUMBER(6)         CONSTRAINT pk_promo_id  PRIMARY KEY,
PROMO_NAME            VARCHAR2(30)      NOT NULL,
PROMO_SUBCATEGORY_ID  NUMBER(2)         NOT NULL,
PROMO_CATEGORY_ID     NUMBER(2)         NOT NULL,
PROMO_COST            NUMBER(10,2)      NOT NULL,
PROMO_BEGIN_DATE      DATE,
PROMO_END_DATE        DATE,
CONSTRAINT fk_promo_cate_id            FOREIGN KEY (PROMO_CATEGORY_ID, PROMO_SUBCATEGORY_ID)
                                       REFERENCES promotion_categories(promo_category_id, promo_subcategory_id)
);


-- タスク表 --
CREATE TABLE task (
task_id              NUMBER(4)                CONSTRAINT pk_task PRIMARY KEY,
base_on              NUMBER(4)                CONSTRAINT fk_task_id  REFERENCES task(task_id),
task_mgr_id          NUMBER(6)                CONSTRAINT fk_person_id REFERENCES person(personid),
expire               DATE
);


