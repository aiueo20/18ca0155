class SampleLiteral03{
    public static void main(String[] args){
        System.out.println(999999999999999.1F);

        System.out.println(2.1E3F);

        System.out.println();

        System.out.println(12.2f);

        System.out.println(2.1D);

        System.out.println(12.2d);

        System.out.println(19.9);

        System.out.println(2.1E3);

        System.out.println(2.1E30);
    }
}